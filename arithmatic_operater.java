import java.util.Scanner;
public class arithmatic_operater {
    public static void main(String[] args)
    {
        Scanner scc=new Scanner(System.in);
        int a= scc.nextInt();
        int b= scc.nextInt();
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/(float)b);
        System.out.println(a%b);

    }




}
